package com.rramalho.surfstore.boards.rest;

import com.rramalho.surfstore.boards.Persistence;

import javax.servlet.http.HttpServletResponse;
import javax.ws.rs.GET;
import javax.ws.rs.Path;
import javax.ws.rs.Produces;
import javax.ws.rs.core.Context;
import javax.ws.rs.core.MediaType;
import java.util.List;

@Path("/boards")
public class Boards {

    @GET
    @Produces(MediaType.APPLICATION_JSON)
    public List<com.rramalho.surfstore.boards.Board> list(@Context HttpServletResponse response){
        response.setHeader("Access-Control-Allow-Origin", "*");

        return Persistence.listBoards();
    }

}
